class Example3 {
	public static void main(String args[]) {
		int var1;
		double var2;
		
		var1 = 10;
		var2 = 10.0;
		
		System.out.println("Original value of var1: " + var1);
		System.out.println("Original value of var2: " + var2);
		System.out.println();
		
		var1 = var1 / 4;
		var2 = var2 / 4;
		
		System.out.println("var1 after division: " + var1);
		System.out.println("var2 after division: " + var2);
	}
}